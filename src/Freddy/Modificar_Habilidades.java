    public void buscar(ObjectContainer basep) {//cargardatos

        Modificarjb.setEnabled(false);
        String IDAux;
        IDAux = cod_habilidades.getText();

        Crear_Habilidades EAux = new Crear_Habilidades();

        if (cod_habilidades.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Ingrese un ID");
        } else {

            if (EAux.verificar(basep, IDAux) == 0) {

                JOptionPane.showMessageDialog(null, "La pintura no existe en la base de datos");
                LimpiarCampos();

            } else {

                Habilidades Ebuscar = new Habilidades(IDAux, null, null);
                ObjectSet result = basep.get(Ebuscar);
                for (int i = 0; i < result.size(); i++) {

                    Habilidades miE = new Habilidades();

                    miE = (Habilidades) result.get(i);

                    cod_habilidades.setText(miE.getCod_habilidades());
                    habilidad_txt.setText(miE.getHabilidad());
                    descripcion_txt.setText(miE.getDescripcion());

                    Modificarjb.setEnabled(true);
                    //Hacer editable los campos de texto
                    mostrarDatos(result);
                    HabilitarCampos_deTexto();
                    cod_habilidades.setEditable(false);

                }

            }

        }
    }

    public void LimpiarCampos() {

        cod_habilidades.setText("");
        habilidad_txt.setText("");
        descripcion_txt.setText("");

    }

    public void HabilitarCampos_deTexto() {
        habilidad_txt.setEditable(true);
        descripcion_txt.setEditable(true);

    }

    public void Modificar_pintura(ObjectContainer basep) {

        Habilidades Emodi = new Habilidades(cod_habilidades.getText(), null, null);
        ObjectSet result = basep.get(Emodi);
        Habilidades Emodificar = (Habilidades) result.next();

        Emodificar.setHabilidad(habilidad_txt.getText());
        Emodificar.setDescripcion(descripcion_txt.getText());
        ;
        basep.set(Emodificar);
        JOptionPane.showMessageDialog(null, "La pintura fue modificado exitosamente");

        mostrarDatos(result);
        LimpiarCampos();
    }

    public void mostrarDatos(ObjectSet result) {
        DefaultTableModel model = (DefaultTableModel) jtableregistro.getModel();
        model.setRowCount(0); // Limpiar la tabla

        if (result.size() == 0) {
            JOptionPane.showMessageDialog(null, "El usuario no existe");
        } else {
            while (result.hasNext()) {
                Habilidades miHabilidades = (Habilidades) result.next();
                Object[] fila = {
                    miHabilidades.getCod_habilidades(),
                    miHabilidades.getHabilidad(),
                    miHabilidades.getDescripcion(),};

                model.addRow(fila);
            }
        }
    }

    public static void Cerrar_BD(ObjectContainer basep) {

        basep.close();
    }

