    public void buscar(ObjectContainer basep) {//cargardatos

        btnmodificar.setEnabled(false);
        String IDAux;
        IDAux = txtcodP.getText();

        Pais_crud EAux = new Pais_crud();

        if (txtcodP.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Ingrese un ID");
        } else {

            if (EAux.comprobarPais(basep, IDAux) == 0) {

                JOptionPane.showMessageDialog(null, "El Pais no existe en la base de datos");
                LimpiarCampos();

            } else {

                Pais Ebuscar = new Pais(IDAux, null, 0);

                ObjectSet result = basep.get(Ebuscar);
                for (int i = 0; i < result.size(); i++) {
                    
                    Pais miE = new Pais();
                    
                    miE = (Pais) result.get(i);   
                    txtcodP.setText(miE.getCodigo_pais());
                    txtnombre.setText(miE.getNombre_pais());                    
                    txtpoblacion.setText(String.valueOf(miE.getPoblacion()));
                    btnmodificar.setEnabled(true);
                    //Hacer editable los campos de texto
//                    mostrarDatos(result);
                    HabilitarCampos_deTexto();
                    txtcodP.setEditable(false);
                }

            }

        }
    }
    public void Modificar_Pais(ObjectContainer basep) {

        Pais Emodi = new Pais(txtcodP.getText(), null,  0 );
        ObjectSet result = basep.get(Emodi);
        
        Pais Emodificar = (Pais) result.next();
        Emodificar.setNombre_pais(txtnombre.getText());
        Emodificar.setPoblacion(Integer.parseInt(txtpoblacion.getText()));

        basep.set(Emodificar);
        JOptionPane.showMessageDialog(null, "El pais fue modificado exitosamente");
//        mostrarDatos(result);
        LimpiarCampos();
    }    
    
